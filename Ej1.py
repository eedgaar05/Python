#Rango de 2 al 20 imprimiendo de dos en dos
b = range(10);
for i in b:
    print((i+1)*2);

#Rango del 20 al 38 imrimiendo de dos en dos
c = range(10);
for i in c:
    print((i*2)+10);

#Rango del 10 al 30 imprimiendo de 4 en 4
d = range(6);
